package com.example.bitskins.bean.InventoryOnSaleBean.ItemBean;

public class Pattern_info {
        private int paintindex;
        private int paintseed;
        private int rarity;
        private int quality;
        private long paintwear;
        private String patternname;
        public void setPaintindex(int paintindex) {
            this.paintindex = paintindex;
        }
        public int getPaintindex() {
            return paintindex;
        }

        public void setPaintseed(int paintseed) {
            this.paintseed = paintseed;
        }
        public int getPaintseed() {
            return paintseed;
        }

        public void setRarity(int rarity) {
            this.rarity = rarity;
        }
        public int getRarity() {
            return rarity;
        }

        public void setQuality(int quality) {
            this.quality = quality;
        }
        public int getQuality() {
            return quality;
        }

        public void setPaintwear(long paintwear) {
            this.paintwear = paintwear;
        }
        public long getPaintwear() {
            return paintwear;
        }

        public void setPatternname(String patternname) {
            this.patternname = patternname;
        }
        public String getPatternname() {
            return patternname;
        }


}
